package Loop2;

import java.util.Scanner;

public class Loop541_self1 {
    static Scanner sc = new Scanner(System.in);

    public static void main(String[] args) {
        char inputChar = sc.next().charAt(0);
        for (int i = 0; i < 20; i++) {
            System.out.print(inputChar);
        }
    }
}

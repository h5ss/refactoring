package Loop1;

import java.util.Scanner;

public class Loop125_ex {
    static Scanner sc = new Scanner(System.in);

    public static void main(String[] args) {
        int inputNum = sc.nextInt();
        int countNum = 0;
        while (inputNum != countNum) {
            countNum++;
            System.out.print(countNum + " ");
        }
    }
}
